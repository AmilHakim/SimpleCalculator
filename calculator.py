print('2. SUBTRACT')
print('3. MULTIPLY')
print('4. DIVIDE')

operation = input()

if operation == '1':
    x = input('Enter the first number: ')
    y = input('Enter the second number: ')
    print(str(int(x) + int(y)))
elif operation == '2':
    x = input('Enter the first number: ')
    y = input('Enter the second number: ')
    print(str(int(x) - int(y)))
elif operation == '3':
    x = input('Enter the first number: ')
    y = input('Enter the second number: ')
    print(str(int(x) * int(y)))
elif operation == '4':
    x = input('Enter the first number: ')
    y = input('Enter the second number: ')
    print(str(int(x) / int(y)))
else:
    print('Entry Invalid. Pick 1 - 4')
